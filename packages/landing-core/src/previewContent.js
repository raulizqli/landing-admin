function hashSeed(seed) {
  const text = String(seed ?? 'preview');
  let hash = 0;
  for (let i = 0; i < text.length; i += 1) {
    hash = (hash * 31 + text.charCodeAt(i)) >>> 0;
  }
  return hash;
}

function pickFrom(arr, seed, offset = 0) {
  return arr[(seed + offset) % arr.length];
}

function hasHeroContent(data) {
  if (Array.isArray(data?.heroSlides) && data.heroSlides.length > 0) {
    return data.heroSlides.some((slide) => (
      String(slide.imageUrl ?? slide.imagenUrl ?? '').trim()
      || String(slide.videoUrl ?? '').trim()
      || (slide.showTitle && String(slide.title ?? '').trim())
      || (slide.showText && String(slide.text ?? slide.texto ?? '').trim())
    ));
  }

  return Boolean(String(data?.heroTitle ?? '').trim() || String(data?.heroSubtitle ?? '').trim());
}

export function isPageEmpty(data) {
  if (!data) return true;
  if (hasHeroContent(data)) return false;
  return PAGE_FIELDS.every((field) => !String(data[field] ?? '').trim());
}

